package com.yxw.framework.common.http;

import java.io.Serializable;

/**
 * 数组形式的请求参数
 * @Package: com.yxw.framework.common.http
 * @ClassName: PostParameterArray
 * @Statement: <p></p>
 * @JDK version used: 
 * @Author: 申午武
 * @Create Date: 2015年6月15日
 * @modify By:
 * @modify Date:
 * @Why&What is modify:
 * @Version: 1.0
 */
public class PostParameterArray implements Serializable {

	private static final long serialVersionUID = 2508066068993652708L;

	private String name;
	private String[] value;

	public PostParameterArray() {
		super();
	}

	/**
	 * @param name
	 * @param value
	 */
	public PostParameterArray(String name, String[] value) {
		super();
		this.name = name;
		this.value = value;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the value
	 */
	public String[] getValue() {
		return value;
	}

	/**
	 * @param value the value to set
	 */
	public void setValue(String[] value) {
		this.value = value;
	}

}
