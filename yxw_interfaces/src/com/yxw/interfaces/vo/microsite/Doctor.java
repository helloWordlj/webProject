package com.yxw.interfaces.vo.microsite;

import java.io.Serializable;

import com.yxw.interfaces.vo.Reserve;

/**
 * 微网站-->医生列表
 * @Package: com.yxw.interfaces.entity.microsite
 * @ClassName: Doctor
 * @Statement: <p></p>
 * @JDK version used: 
 * @Author: 申午武
 * @Create Date: 2015年6月26日
 * @modify By:
 * @modify Date:
 * @Why&What is modify:
 * @Version: 1.0
 */
public class Doctor extends Reserve implements Serializable {

	private static final long serialVersionUID = -5926457842875864862L;
	/**
	 * 医院代码,医院没有分院则返回空字符串
	 */
	private String branchCode;
	/**
	 * 医院名称,医院没有分院则返回空字符串
	 */
	private String branchName;
	/**
	 * 科室代码
	 */
	private String deptCode;
	/**
	 * 科室名称
	 */
	private String deptName;
	/**
	 * 医生代码,若只存在医生代码或只存在医生工号，则两者返回相同的值
	 */
	private String doctorCode;
	/**
	 * 医生工号
	 */
	private String doctorNo;
	/**
	 * 医生名称
	 */
	private String doctorName;
	/**
	 * 医生性别
	 */
	private String doctorSex;
	/**
	 * 出生日期
	 */
	private String doctorBirth;
	/**
	 * 医生电话
	 */
	private String doctorTelephone;
	/**
	 * 医生擅长
	 */
	private String doctorSkill;
	/**
	 * 医生简介
	 */
	private String doctorIntrodution;
	/**
	 * 医生职称
	 */
	private String doctorTitle;
	/**
	 * 照片,可供外网访问的url路径(要jpg，200X200px以上大小，最好是生活照（医生自己觉得自己最满意的照片）。如果没有，工作照也可以)
	 */
	private String picture;
	
	public Doctor() {
		super();
	}

	public Doctor(String branchCode, String branchName, String deptCode,
			String deptName, String doctorCode, String doctorNo,
			String doctorName, String doctorSex, String doctorBirth,
			String doctorTelephone, String doctorSkill,
			String doctorIntrodution, String doctorTitle, String picture) {
		super();
		this.branchCode = branchCode;
		this.branchName = branchName;
		this.deptCode = deptCode;
		this.deptName = deptName;
		this.doctorCode = doctorCode;
		this.doctorNo = doctorNo;
		this.doctorName = doctorName;
		this.doctorSex = doctorSex;
		this.doctorBirth = doctorBirth;
		this.doctorTelephone = doctorTelephone;
		this.doctorSkill = doctorSkill;
		this.doctorIntrodution = doctorIntrodution;
		this.doctorTitle = doctorTitle;
		this.picture = picture;
	}

	public String getBranchCode() {
		return branchCode;
	}

	public void setBranchCode(String branchCode) {
		this.branchCode = branchCode;
	}

	public String getBranchName() {
		return branchName;
	}

	public void setBranchName(String branchName) {
		this.branchName = branchName;
	}

	public String getDeptCode() {
		return deptCode;
	}

	public void setDeptCode(String deptCode) {
		this.deptCode = deptCode;
	}

	public String getDeptName() {
		return deptName;
	}

	public void setDeptName(String deptName) {
		this.deptName = deptName;
	}

	public String getDoctorCode() {
		return doctorCode;
	}

	public void setDoctorCode(String doctorCode) {
		this.doctorCode = doctorCode;
	}

	public String getDoctorNo() {
		return doctorNo;
	}

	public void setDoctorNo(String doctorNo) {
		this.doctorNo = doctorNo;
	}

	public String getDoctorName() {
		return doctorName;
	}

	public void setDoctorName(String doctorName) {
		this.doctorName = doctorName;
	}

	public String getDoctorSex() {
		return doctorSex;
	}

	public void setDoctorSex(String doctorSex) {
		this.doctorSex = doctorSex;
	}

	public String getDoctorBirth() {
		return doctorBirth;
	}

	public void setDoctorBirth(String doctorBirth) {
		this.doctorBirth = doctorBirth;
	}

	public String getDoctorTelephone() {
		return doctorTelephone;
	}

	public void setDoctorTelephone(String doctorTelephone) {
		this.doctorTelephone = doctorTelephone;
	}

	public String getDoctorSkill() {
		return doctorSkill;
	}

	public void setDoctorSkill(String doctorSkill) {
		this.doctorSkill = doctorSkill;
	}

	public String getDoctorIntrodution() {
		return doctorIntrodution;
	}

	public void setDoctorIntrodution(String doctorIntrodution) {
		this.doctorIntrodution = doctorIntrodution;
	}

	public String getDoctorTitle() {
		return doctorTitle;
	}

	public void setDoctorTitle(String doctorTitle) {
		this.doctorTitle = doctorTitle;
	}

	public String getPicture() {
		return picture;
	}

	public void setPicture(String picture) {
		this.picture = picture;
	}
	
}
